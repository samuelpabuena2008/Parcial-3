    @SuppressWarnings("unchecked")
    private void refreshComboBoxes() {
        cmbSelectUser.removeAllItems();
        cmbSelectUser.addItem("Select one");

        cmbSelectUserSecondary.removeAllItems();
        cmbSelectUserSecondary.addItem("Select one");

        Response docResp = patientController.getDoctorList();
        if (docResp.getStatus() == Status.OK && docResp.getData() instanceof List) {
            for (Map<String, String> d : (List<Map<String, String>>) docResp.getData()) {
                cmbSelectUser.addItem(d.get("id"));
            }
        }

        Response patResp = doctorController.getPatientList();
        if (patResp.getStatus() == Status.OK && patResp.getData() instanceof List) {
            for (Map<String, String> p : (List<Map<String, String>>) patResp.getData()) {
                cmbSelectUserSecondary.addItem(p.get("id"));
            }
        }
    }

