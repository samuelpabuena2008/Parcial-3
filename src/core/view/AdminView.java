    private void btnGoToDoctorViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoToDoctorViewActionPerformed
        if (cmbSelectUser.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        long selectedId = Long.parseLong(cmbSelectUser.getItemAt(cmbSelectUser.getSelectedIndex()));
        Response resp = doctorController.getDoctorData(selectedId);
        if (resp.getStatus() == Status.OK) {
            DoctorView doctor = new DoctorView(database, adminId, selectedId, true);
            database.removeObserver(this);
            this.dispose();
            doctor.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "El usuario seleccionado no es un Doctor.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGoToDoctorViewActionPerformed

